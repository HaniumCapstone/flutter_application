

class HistoricSite {
  final int? siteId; // Nullable type
  final int? characterId; // Nullable type
  final String? siteName; // Nullable type
  final String? siteLink; // Nullable type
  final String? imageLink; // Nullable type
  final String? siteDescription; // Nullable type
  final double? longitude; // Nullable type
  final double? latitude; // Nullable type

  HistoricSite({
    this.siteId,
    this.characterId,
    this.siteName,
    this.siteLink,
    this.imageLink,
    this.siteDescription,
    this.longitude,
    this.latitude,
  });
}
